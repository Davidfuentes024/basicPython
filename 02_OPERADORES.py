###  OPERADORES  ###
#OPERADORES CON NUMEROS
print(1 + 2)     #suma
print(1 - 2)     #resta
print(1 * 2)     #multiplicar
print(1 / 2)     #dividir
print(10 % 2)    #resiuo 
print(5 // 2)    #division_aprox_entera
print(2 ** 3)    #elevar_num1_al_num2

#CONCATENAR
print("hola" + "python")
print("1+2 es igual a " + str(3))
print("pene " * 3)

#OPERADORES COMPARATIVOS
print(2 > 3) #mayor que
print(2 < 3) #menor que
print(2 >= 3) #mayor o igual que 
print(2 <= 3) #menor o igual que
print(2 == 3) #comparar igualdad
print(2 != 3) #comparar diferencia

#CON STRINGS COMPARA ORDEN ALFABETICA TENIENDO EN CUENTA MAYUSCULAS
print("abc" > "ABC")
print("abc" != "ABC")
print("abc" == "ABC")

    ### comparadores solo nos da valores booleanos (TRUE o FALSE)  ###

